extension DataCollectionViewController {

  /// Specifies how cells are aligned in the collection view.
  public enum CellAlignment {

    /// Cells are aligned to the start of the collection view, i.e. left for horizontal orientation
    /// and top for vertical orientation.
    case start

    /// Cells are aligned to the center of the collection view.
    case center

    /// Cells are aligned to the end of the collection view, i.e. right for horizontal orientation
    /// and bottom for vertical orientation.
    case end
  }
}
