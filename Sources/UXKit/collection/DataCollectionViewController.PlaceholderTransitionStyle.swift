extension DataCollectionViewController {

  /// Transition style for the placeholder view in a `DataCollectionViewController`.
  public enum PlaceholderTransitionStyle {

    /// No transitions.
    case none

    /// Fade in.
    case fade

    /// Slide up.
    case slideUp

    /// Slide right.
    case slideRight

    /// Slide down.
    case slideDown

    /// Slide left.
    case slideLeft
  }
}
